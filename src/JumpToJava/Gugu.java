package JumpToJava;

public class Gugu {
    public static void main(String[] args) {
        int a;
        int b;
        for (a = 2; a < 10; a++){
            for(b = 1; b<10; b++){
                System.out.println(a+"X"+b+"="+a*b+"입니다.");
            }
            System.out.println();
        }
    }
}